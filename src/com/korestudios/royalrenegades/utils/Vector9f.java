package com.korestudios.royalrenegades.utils;

public class Vector9f {
    public float x,y,w,h,uvx,uvy,uvw,uvh,rot;

    public Vector9f(float x, float y, float w, float h, float uvx, float uvy, float uvw, float uvh, float rot) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
        this.uvx = uvx;
        this.uvy = uvy;
        this.uvw = uvw;
        this.uvh = uvh;
        this.rot=rot;
    }
}
