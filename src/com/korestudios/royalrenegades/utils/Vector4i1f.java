package com.korestudios.royalrenegades.utils;

public class Vector4i1f {
    public int x1, y1, x2, y2;
    public float rot;

    public Vector4i1f(int x1, int y1, int x2, int y2, float rot) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.rot = rot;
    }
}
